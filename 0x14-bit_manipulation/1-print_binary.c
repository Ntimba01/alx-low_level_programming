#include "main.h"

/*
 * unsigned long integer (n) to the standard output.
 * print_binary - prints the number
 * @n- represent the number to be converted to binary and printed.
 */
void print_binary(unsigned long int n)
{
	int i, count = 0;
	unsigned long int current;

	for (i = 63; i >= 0; i--)
	{
		current = n >> i;

		if (current & 1)
		{
			_putchar('1');
			count++;
		}
		else if (count)
			_putchar('0');
	}
	if (!count)
		_putchar('0');
}
